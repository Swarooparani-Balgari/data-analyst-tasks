--- VIEW DATA ---

SELECT * FROM SESSIONS;

--- OVERALL CONVERSION ---

SELECT 
  COUNT(CASE WHEN PURCHASED = 'YES' THEN 1 END)*100.0/COUNT(*) AS CONVERSION_RATE
FROM SESSIONS;

--- TRAFFICSOURCE PERFORMANCE ---

SELECT 
  TRAFFICSOURCE,
  COUNT(*) AS TOTAL_SESSIONS,
  SUM(CASE WHEN PURCHASED = 'YES' THEN 1 ELSE 0 END) AS PURCHASES
FROM SESSIONS
GROUP BY TRAFFICSOURCE
ORDER BY PURCHASES;

--- CART DROP-OFF ---

SELECT
  COUNT(CASE WHEN ADDEDTOCART = 'YES' THEN 1 END) AS CARTS,
  COUNT(CASE WHEN PURCHASED = 'YES' THEN 1 END) AS PURCHASES
FROM SESSIONS;

--- AVG TIMEONSITE_SEC ---

SELECT 
 TRAFFICSOURCE,
 AVG(TIMEONSITE_SEC) AS AVG_TIMEONSITE_SEC
FROM SESSIONS
GROUP BY TRAFFICSOURCE
ORDER BY AVG_TIMEONSITE_SEC DESC;

--- PURCHASES BY DEVICE ---

SELECT
 DEVICE,
 COUNT(CASE WHEN PURCHASED = 'YES' THEN 1 END) AS PURCHASES
FROM SESSIONS
GROUP BY DEVICE
ORDER BY PURCHASES DESC;

--- PURCHASES BY DATE ---

SELECT 
  DATE,
  COUNT(CASE WHEN PURCHASED='YES' THEN 1 END) AS PURCHASES
FROM SESSIONS
GROUP BY DATE;