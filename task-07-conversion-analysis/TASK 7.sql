DATE DATE,
TRAFFICSOURCE VARCHAR(20),
DEVICE VARCHAR(20),
PAGESVIEWED INT,
TIMEONSITE_SEC INT,
ADDEDTOCART VARCHAR(20),
PURCHASED VARCHAR(20)
);

