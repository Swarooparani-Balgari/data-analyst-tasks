SELECT * FROM CUSTOMERS;

--- OVERALL CHURN RATE ---

SELECT 
 AVG(CASE WHEN CHURNED='YES' THEN 1 ELSE 0 END)*100 AS CHURN_RATE
FROM CUSTOMERS;

--- PLAN-WISE CHURN RATE ---

SELECT 
 PLAN_TYPE,
 AVG(CASE WHEN CHURNED='YES' THEN 1 ELSE 0 END)*100 AS CHURN_RATE
FROM CUSTOMERS
GROUP BY PLAN_TYPE
ORDER BY CHURN_RATE DESC;

---COMPLAINT IMPACT ---

SELECT 
 CHURNED,
 AVG(CALLDROP_COMPLAINTS) AS AVG_COMPLAINTS
FROM CUSTOMERS
GROUP BY CHURNED
ORDER BY AVG_COMPLAINTS DESC;

--- HIGH-RISK CUSTOMERS ---

SELECT *
FROM CUSTOMERS
WHERE NETWORK_RATING<=2
 AND CALLDROP_COMPLAINTS>=5;

 --- LOW-RISK CUSTOMERS ---

 SELECT *
 FROM CUSTOMERS
 WHERE NETWORK_RATING>2
  AND CALLDROP_COMPLAINTS<5;