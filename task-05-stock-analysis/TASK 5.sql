--- LOW STOCK PRODUCTS ---

SELECT 
  PRODUCT,
  STOCKQTY,
  REORDERLEVEL
FROM STOCKS
WHERE STOCKQTY<REORDERLEVEL;

--- OVER STOCK PRODUCTS ---

SELECT 
 PRODUCT,
 STOCKQTY,
 UNITSSOLD_LAST30DAYS
FROM STOCKS
WHERE STOCKQTY>UNITSSOLD_LAST30DAYS*2;

--- WAREHOUSE PERFORMANCE ---

SELECT
 WAREHOUSE,
 SUM(UNITSSOLD_LAST30DAYS) AS TOTAL_SALES
FROM STOCKS
GROUP BY WAREHOUSE
ORDER BY TOTAL_SALES DESC;

--- PRODUCT PERFORMANCE ---

SELECT 
 PRODUCT,
 SUM(UNITSSOLD_LAST30DAYS) AS TOTAL_SALES
FROM STOCKS
GROUP BY PRODUCT
ORDER BY TOTAL_SALES DESC;

--- UNITSSOLD BY CATEGORY ---

SELECT 
 CATEGORY,
 SUM(UNITSSOLD_LAST30DAYS) AS TOTAL_UNITS
FROM STOCKS
GROUP BY CATEGORY
ORDER BY TOTAL_UNITS DESC;

--- AVG SUPPLIERLEAD DAYS BY PRODUCT ---

SELECT 
  PRODUCT,
  AVG(SUPPLIERLEAD_TIMEDAYS) AS AVG_SUPPLIERLEAD_TIMEDAYS
FROM STOCKS
GROUP BY PRODUCT
ORDER BY AVG_SUPPLIERLEAD_TIMEDAYS DESC;