PRODUCTID INT PRIMARY KEY,
PRODUCT VARCHAR(20),
CATEGORY VARCHAR(20),
WAREHOUSE VARCHAR(3),
STOCKQTY INT,
REORDERLEVEL INT,
UNITSSOLD_LAST30DAYS INT,
SUPPLIERLEAD_TIMEDAYS INT
);

