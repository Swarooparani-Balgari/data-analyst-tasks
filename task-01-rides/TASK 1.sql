---- CALCUALTE FARE_PRICE / REVENUE ----

SELECT 
 SUM(DISTANCE_KM*FAREPERKM*SURGE_MULTIPLIER) AS REVENUE
FROM RIDES;

---- REVENUE BY CITY ----

SELECT 
 CITY,
 SUM(DISTANCE_KM*FAREPERKM*SURGE_MULTIPLIER) AS REVENUE
FROM RIDES
GROUP BY CITY
ORDER BY REVENUE DESC;

---- AVERAGE RATING ----

SELECT
  VEHICLETYPE,
  AVG(DRIVER_RATING) AS AVG_RATING
FROM RIDES
GROUP BY VEHICLETYPE
ORDER BY AVG_RATING DESC;

---- PEAK SURGE ANALYSIS ----

SELECT
  SURGE_MULTIPLIER,
  COUNT(*) AS TRIP_COUNT
FROM RIDES
GROUP BY SURGE_MULTIPLIER
ORDER BY SURGE_MULTIPLIER DESC;

---- REVENUE BY VEHICLE_TYPE ----

SELECT 
 VEHICLETYPE,
 SUM(DISTANCE_KM*FAREPERKM*SURGE_MULTIPLIER) AS REVENUE
FROM RIDES
GROUP BY VEHICLETYPE
ORDER BY REVENUE DESC;

---- REVENUE BY TRIPDATE ----

SELECT 
 TRIPDATE,
 SUM(DISTANCE_KM*FAREPERKM*SURGE_MULTIPLIER) AS REVENUE
FROM RIDES
GROUP BY TRIPDATE 
ORDER BY TRIPDATE ;

---- REVENUE BY PAYMENT_MODE ----

SELECT 
 PAYMENT_MODE,
 SUM(DISTANCE_KM*FAREPERKM*SURGE_MULTIPLIER) AS REVENUE
FROM RIDES
GROUP BY PAYMENT_MODE
ORDER BY REVENUE ;

