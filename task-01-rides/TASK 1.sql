TRIPID INT PRIMARY KEY,
TRIPDATE DATE,
CITY VARCHAR(20),
VEHICLETYPE VARCHAR(20),
DISTANCE_KM INT,
FAREPERKM INT,
SURGE_MULTIPLIER FLOAT,
DRIVER_RATING FLOAT,
PAYMENT_MODE VARCHAR(20)
);

