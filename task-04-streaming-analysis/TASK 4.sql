--- CREATE TABLE ---

CREATE TABLE PLATFORM(
USERID INT,
JOINDATE DATE,
COUNTRY VARCHAR(20),
SUBSCRIPTIONTYPE VARCHAR(20),
MONTHLYFEE INT,
WATCHHOURS INT,
DEVICESUSED INT,
CHURNED VARCHAR(20),
PRIMARY KEY (USERID,JOINDATE)
);

