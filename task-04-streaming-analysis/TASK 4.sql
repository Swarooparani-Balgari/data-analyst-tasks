--- TOTAL REVENUE ---

SELECT
  SUM(MONTHLYFEE) AS MONTHLYREVENUE
FROM PLATFORM;

--- REVENUE BY PLAN ---

SELECT 
  SUBSCRIPTIONTYPE,
  SUM(MONTHLYFEE) AS MONTHLYREVENUE
FROM PLATFORM
GROUP BY SUBSCRIPTIONTYPE
ORDER BY MONTHLYREVENUE DESC;

--- CHURN ANALYSIS ---

SELECT
  SUBSCRIPTIONTYPE,
  COUNT(*) AS TOTAL_USERS,
  SUM(CASE WHEN CHURNED = 'YES' THEN 1 ELSE 0 END) AS CHURNED_USERS
FROM PLATFORM
GROUP BY SUBSCRIPTIONTYPE
ORDER BY CHURNED_USERS DESC;

--- ENGAGEMENT ---

SELECT 
  COUNTRY,
  AVG(WATCHHOURS) AS AVG_WATCHHOURS
FROM PLATFORM
GROUP BY COUNTRY
ORDER BY AVG_WATCHHOURS DESC;

--- REVENUE BY JOIN DATE ---

SELECT 
  JOINDATE,
  SUM(MONTHLYFEE) AS MONTHLYREVENUE
FROM PLATFORM
GROUP BY JOINDATE;

