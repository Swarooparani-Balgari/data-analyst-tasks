--- CREATE TABLE ---

CREATE TABLE LOANS(
CUSTOMERID INT PRIMARY KEY,
AGE INT,
INCOME INT,
LOANAMOUNT INT,
LOANTERM_MONTHS INT,
CREDITSCORE INT,
EMPLOYMENTSTATUS VARCHAR(20),
DEFAULTED VARCHAR(20)
);

