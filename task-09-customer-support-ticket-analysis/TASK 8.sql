--- VIEW DATA ---

SELECT * FROM LOANS;

--- DEFAULT RATE ---

SELECT
  COUNT(CASE WHEN DEFAULTED='YES' THEN 1 END)*100.0/COUNT(*) AS DEFAULT_RATE
FROM LOANS;

--- EMPLOYMENT RISK ---

SELECT 
 EMPLOYMENTSTATUS,
 COUNT(*) AS TOTAL,
 SUM(CASE WHEN DEFAULTED='YES' THEN 1 ELSE 0 END) AS DEFAULTS
FROM LOANS
GROUP BY EMPLOYMENTSTATUS
ORDER BY TOTAL DESC;

--- CREDIT SCORE IMPACT ---

SELECT 
 DEFAULTED,
 AVG(CREDITSCORE) AS AVG_CREDITSCORE
FROM LOANS
GROUP BY DEFAULTED
ORDER BY AVG_CREDITSCORE DESC;

--- HIGH-RISK CUSTOMERS ---

SELECT *
FROM LOANS
WHERE CREDITSCORE<650;

--- MEDIUM-RISK CUSTOMERS ---

SELECT *
FROM LOANS
WHERE CREDITSCORE<=700;

--- LOW-RISK CUSTOMERS ---

SELECT *
FROM LOANS
WHERE CREDITSCORE>700;

--- DEFAULT RATE BY EMPLOYMENT STATUS ---

SELECT 
  EMPLOYMENTSTATUS,
  (COUNT(CASE WHEN DEFAULTED = 'YES' THEN 1 END)*100.0/COUNT(*)) AS DEFAULT_RATE
FROM LOANS
GROUP BY EMPLOYMENTSTATUS
ORDER BY DEFAULT_RATE DESC;
