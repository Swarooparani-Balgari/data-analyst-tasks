--- TOTAL REVENUE ---

SELECT 
  SUM(QUANTITY*UNITPRICE-(UNITPRICE*DISCOUNTPERCENT/100)) AS TOTAL_REVENUE
FROM COMMERCE;

--- REVENUE BY CATEGORY ---

SELECT
  PRODUCTCATEGORY,
  SUM(QUANTITY*UNITPRICE-(UNITPRICE*DISCOUNTPERCENT/100)) AS TOTAL_REVENUE
FROM COMMERCE
GROUP BY PRODUCTCATEGORY
ORDER BY TOTAL_REVENUE DESC;

--- REVENUE BY REGION ---

SELECT
  REGION,
  SUM(QUANTITY*UNITPRICE-(UNITPRICE*DISCOUNTPERCENT/100)) AS TOTAL_REVENUE
FROM COMMERCE
GROUP BY REGION
ORDER BY TOTAL_REVENUE DESC;

--- REVENUE BY PRODUCT ---

SELECT 
  PRODUCT,
  SUM(QUANTITY*UNITPRICE-(UNITPRICE*DISCOUNTPERCENT/100)) AS TOTAL_REVENUE
FROM COMMERCE
GROUP BY PRODUCT
ORDER BY TOTAL_REVENUE DESC;

--- REVENUE BY ORDER DATE ---

SELECT 
  ORDERDATE,
  SUM(QUANTITY*UNITPRICE-(UNITPRICE*DISCOUNTPERCENT/100)) AS TOTAL_REVENUE
FROM COMMERCE
GROUP BY ORDERDATE;